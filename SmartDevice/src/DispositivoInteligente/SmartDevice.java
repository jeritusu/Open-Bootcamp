
package DispositivoInteligente;


public class SmartDevice {

    String tipoPantalla;
    int autonomia; 
    boolean gps;
    public double velocidadProcesador;
    int ram;

    public SmartDevice() {
    }

    public SmartDevice(String tipoPantalla, int autonomia, boolean gps, double velocidadProcesador, int ram) {
        this.tipoPantalla = tipoPantalla;
        this.autonomia = autonomia;
        this.gps = gps;
        this.velocidadProcesador = velocidadProcesador;
        this.ram = ram;
    } 
    

    
    
    
    
  
    
}
